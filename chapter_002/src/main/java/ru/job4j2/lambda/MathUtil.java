package ru.job4j2.lambda;

/**
 * Class MathUtil.
 *
 * @author vivanov
 * @version 1
 * @since 23.09.2019
 */
public class MathUtil {

    /**
     * Method to return result of sum.
     *
     * @param left first value
     * @param second second value
     * @return result sum
     */
    public static double add(int left, int second) {
        return left + second;
    }

    /**
     * Method to return result of division.
     *
     * @param left first value
     * @param second second value
     * @return result of division
     */
    public static double div(int left, int second) {
        return left / second;
    }


}
